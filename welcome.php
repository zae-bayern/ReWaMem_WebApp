<h2>
Herzlich Willkommen zum</br>
<span>Onlinetool Ökobench für die Textilpflegebranche</span>
</h2>

<p>
Im Rahmen eines DBU-Projekts wurde ein Onlinetool zum Vergleich der Energieverbräuche in Wäschereien, Reinigungen und Mischbetrieben entwickelt.
Wir laden Sie herzlich ein, Ihre Verbräuche im Vergleich zur Branche zu betrachten, um Optimierungsbedarf zu erkennen.
</p>

<p>
Dieses Tool wertet alle Daten <b>anonymisiert</b> aus und steht allen Betrieben der Textilpflegebranche - unabhängig davon, ob sie Mitglied im DTV sind oder nicht - kostenfrei zur Verfügung.
</p>

<p>
